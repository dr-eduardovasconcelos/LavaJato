package br.recife.edu.ifpe.lavajato.model.classes;

public class TipoCarro {
	
	private int codigo;
	private String tipo;
	private String descricao;
	private double valorComum;
	private double valorCompleta;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public double getValorComum() {
		return valorComum;
	}
	public void setValorComum(double valorComum) {
		this.valorComum = valorComum;
	}
	public double getValorCompleta() {
		return valorCompleta;
	}
	public void setValorCompleta(double valorCompleta) {
		this.valorCompleta = valorCompleta;
	}
	
	

}
